    public void mostrarDato() {
        
        conexion objConexion = new conexion();
        
        String strSentenciaSelect = String.format("select * from cuerpoAgua where id = %s", idSearchField.getText());
                
        try {
            
            ResultSet resultado = objConexion.consultarRegistros(strSentenciaSelect);
            
            nameField2.setText(resultado.getString("nombre"));
            idField2.setText(resultado.getString("id"));
            municipioField2.setText(resultado.getString("municipio"));
            cuerpoField2.setText(resultado.getString("cuerpo"));
            typeField2.setText(resultado.getString("tipo"));
            ircaField2.setText(resultado.getString("irca"));
            
        }
        
        catch (Exception e) {
            System.out.println(e);
            
        }
    }
        
        
    public void mostrarDatos() {
        
        conexion objConexion = new conexion();
        String[] titulos = {"Nombre", "Id", "Municipio", "Tipo de cuerpo de agua", "Tipo de agua", "IRCA"};
        modelo = new DefaultTableModel(null, titulos);
        queryTable.setModel(modelo);
        
        try {
            
            ResultSet resultado = objConexion.consultarRegistros("select * from cuerpoAgua");
            
            while (resultado.next()) {
                
                
                Object[] oCuerposAgua = {resultado.getString("nombre"), 
                                        resultado.getString("id"),
                                        resultado.getString("municipio"),
                                        resultado.getString("cuerpo"),
                                        resultado.getString("tipo"),
                                        resultado.getString("irca")
                };
                
                
                modelo.addRow(oCuerposAgua);
            }
        }
        catch (Exception e) {
            System.out.println(e);
            
        }
        
        try {
            objConexion.conn.close();
        }
        
        catch (Exception e) {
            System.out.println(e);
        }
    }
        
