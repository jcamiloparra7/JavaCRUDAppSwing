    public CuerpoDeAgua recuperarDatosGUI() {
        
        CuerpoDeAgua oCuerpo = new CuerpoDeAgua();
        
        double id = (idField.getText().isEmpty())?0.0: Double.parseDouble(idField.getText());
        double irca = Double.parseDouble(ircaField.getText());
        oCuerpo.setId(id);
        oCuerpo.setIrca(irca);
        oCuerpo.setNombre(nameField.getText());
        oCuerpo.setMunicipio(municipioField.getText());
        oCuerpo.setTipoCuerpo(cuerpoField.getText());
        oCuerpo.setTipoAgua(typeField.getText());
        
        return oCuerpo;
        
    }
    
    
